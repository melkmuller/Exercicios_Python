#Entrada de dados
print("Vamos transformar uma tempertaura de Fahrenheit para Graus Celsius!")
temp_f = float(input("Informe o valor em Fahrenheit: "))

#Processamento dos dados
temp_c = (temp_f * 1.8) + 32

#Saída de dados
print("Temperatura em graus Fahrenheit = " + str(temp_f) + " °F")
print("Temperatura em graus Celsius = " + str(temp_c) + " °C")